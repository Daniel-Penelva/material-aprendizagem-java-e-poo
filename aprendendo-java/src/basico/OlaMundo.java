package basico;

public class OlaMundo {
	/*main - é o nome do método que vai executar, os métodos decidem como vai ser o tipo de retorno... */
	public static void main(String[] args) {
		/* programa simples que vai imprimir na tela do meu sistema "Olá mundo!" */
		System.out.println("Olá Mundo!");
		
		/*exemplo de caracter tabulação*/
		System.out.println("\t JAVA SE");
		
		/*Exemplo de quebra de linha*/
		System.out.println("Exemplo de:\n quebra de linha");
		
		/*exemplo de aspas*/
		System.out.println("Exemplo de \"Aspas\"no java.");
		
		/*exemplo de barra*/
		System.out.println("Exemplo de \\barra no java.");
		
		/*
		 *   Estes caracteres especiais sempre se escrevem antes da barra, exemplos:
		 *   \t
		 *   \"
		 *   \n
		 *   \\ 
		 * */	

	}

}
